from turtle import *
import turtle
i=1
while(i==1):
    turtle.shape('circle')
    turtle.color("green")
    turtle.width(2)
    circle(120)
    turtle.penup()
    turtle.right(90)
    turtle.forward(20)
    turtle.right(-90)
    i=1
    turtle.shape('circle')
    turtle.color("blue")
    turtle.width(2)
    turtle.pendown()
    circle(140)
